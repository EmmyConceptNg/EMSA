                    <h2 class="text-bold veil reveal-lg-block">Design Concept</h2>
                    <div class="hr divider bg-madison hr-sm-left-0 veil reveal-lg-block"></div>
                    <div class="offset-lg-top-60">
                      <p>The main colours of the 90th anniversary logo are red and blue. The abbreviation of St. Louis School SLS merges with the number ‘90’. The five dots scattered around the number ‘0’ represent the five Houses of our school. There are two sets of concentric circles in the number ‘90’. The former represents the School and the latter represents students. They symbolise the familial atmosphere of freedom and trust cultivated by our school. It also symbolises how our school provides students with the platform and education to become all-rounded individuals who take pride in echoing the Aloysian spirit.</p>
